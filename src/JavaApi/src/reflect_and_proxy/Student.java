package JavaApi.src.reflect_and_proxy;

public class Student {
    private String name;
    private String sex;
    public int i;

    public Student(){}

    public Student(String name, String sex) {
        this.name = name;
        this.sex = sex;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
